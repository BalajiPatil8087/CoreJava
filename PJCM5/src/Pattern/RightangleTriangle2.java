package Pattern;

public class RightangleTriangle2 {

	public static void main(String[] args) 
	{
		int i,j,k;
		for(i=1;i<=5;i++) 
		{
			for (j=1;j<5;j++) 
			{
				System.out.print(" ");
			}
			for (k=i;k<=5;k++) 
			{
				System.out.print("*");
			}
			System.out.print("\n");
			
			
		}
		
	}

}
