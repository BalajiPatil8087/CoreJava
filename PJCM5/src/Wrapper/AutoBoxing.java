package Wrapper;

public class AutoBoxing 
{
	public static void main(String[] args) 
	{
		int a=100;
		Integer i =a;
		System.out.println(a);
		
		System.out.println(i.toString());
		
		int b=i;
		System.out.println(b);
		
		
		char c='a';
		Character s=c;
		System.out.println(c);
		
		char t=s;
		System.out.println(t);
		
		
	}

}
